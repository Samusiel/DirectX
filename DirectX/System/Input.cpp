	void Input::Update()
	{
		// update all devices

		if (this->keyboard.get() != nullptr)
		{
			this->keyboard->Update();
		}

		if (this->mouse.get() != nullptr)
		{
			this->mouse->Update();
		}

		for (size_t idx = 0; idx < Gamepad::MaxConnected; ++idx)
		{
			if (this->gamepad[idx].get() != nullptr)
			{
				this->gamepad[idx]->Update();
			}
		}
	}

